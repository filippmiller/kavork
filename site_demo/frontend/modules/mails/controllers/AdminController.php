  public function actionStart($id, $type)
  {
    if (Yii::$app->user->isGuest || !Yii::$app->user->can('TemplateMailView')) {
      throw new \yii\web\ForbiddenHttpException(Yii::t('app', 'Page does not exist'));
    }
    $request = Yii::$app->request;

    if (!$request->isPost || !$request->isAjax) {
      throw new \yii\web\ForbiddenHttpException(Yii::t('app', 'Page does not exist'));
      return false;
    }

    Yii::$app->response->format = Response::FORMAT_JSON;

    $template = $this->findModel($id);

    $button = Html::button(Yii::t('app', 'Close'), ['class' => 'btn btn-default pull-left', 'data-dismiss' => "modal"]);
    $title = Yii::t('app', 'Sending mails');

    if (!$template) {
      return [
          'title' => $title,
          'content' => Yii::t('app', 'Mail template not found'),
          'footer' => $button
      ];
    }

    $mails = new MailsLog();
    $mails->user_id = Yii::$app->user->id;
    $mails->cafe_id = Yii::$app->cafe->id;
    $mails->cteated_at = date('Y-m-d H:i:s');
    $mails->setTemplate($template);
    $mails->params = json_encode([
        $type => $request->post('pks')
    ]);
    $mails->save();

    return [
        'title' => $title,
        'content' => Yii::t('app', 'Job newsletter created.'),
        'footer' => $button
    ];
  }

