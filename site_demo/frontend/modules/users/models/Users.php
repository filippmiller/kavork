    }

    $roles = $rolesQuery->all();
    if ($isSearch) {
      $roles_array[''] = Yii::t('app', "ALL");
    }

    $roles_array['-1'] = Yii::t('app', "Cafe manager");
    //ddd($allCafeShow_array,$roles);
