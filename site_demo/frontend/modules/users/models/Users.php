      foreach ($roles as $role) {
        $roles_array[$role['name']] = Yii::t('app', "role_" . $role['name']);
        $allCafeShow_array[$role['name']] = ['allCafe' => !is_null($role['showAllCafe'])];
      }
    }

    if ($testAllCafe) {
      return [
          'role' => $roles_array,
          'allCafe' => $allCafeShow_array
      ];
    } else {
      return $roles_array;
    }
  }

