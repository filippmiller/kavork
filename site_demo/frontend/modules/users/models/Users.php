  public function getRoleOfUserArray($id = false)
  {
    if ($id == false) $id = $this->id;
    if (!isset($this->role) || !is_array($this->role)) {
      $roles = (new Query)
          ->select('item_name')
          ->from('auth_assignment')
          ->where(['user_id' => $id])
          ->all();
      $this->role = array();
      if ($roles) {
        foreach ($roles as $role) {
          $this->role[] = $role['item_name'];
        }
      }
    }
    return $this->role;
  }

